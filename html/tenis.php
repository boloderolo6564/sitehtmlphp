    <h3>Tênis</h3>
    <p style ="text-align:left;">O tênis é um esporte praticado individualmente ou em duplas, onde os jogadores utilizam raquetes para rebater uma bola sobre uma quadra dividida ao meio por uma rede. Com raízes que remontam ao século XII na França, o tênis moderno evoluiu ao longo dos séculos para se tornar uma das atividades esportivas mais populares em todo o mundo. O objetivo do jogo é fazer com que a bola passe sobre a rede e caia dentro das linhas da quadra do adversário, ao mesmo tempo em que se evita que a bola rebatida pelo oponente alcance o chão dentro das próprias linhas. Isso requer uma combinação de habilidade, técnica, agilidade e estratégia. O tênis é praticado em diferentes superfícies, como grama, saibro, quadras duras e carpetes, cada uma com suas próprias características e desafios. As partidas podem ser disputadas em uma variedade de formatos, desde jogos individuais simples até torneios de grande escala, como os Grand Slam. Além de ser uma competição esportiva emocionante, o tênis oferece uma série de benefícios físicos e mentais para os praticantes. Ele ajuda a melhorar a coordenação motora, a resistência cardiovascular, a força muscular e a agilidade. Além disso, promove a socialização, o trabalho em equipe (no caso de partidas de duplas) e o desenvolvimento de habilidades mentais, como concentração, disciplina e estratégia. O tênis é um esporte acessível para pessoas de todas as idades e níveis de habilidade, podendo ser praticado recreativamente em clubes locais ou de forma mais competitiva em torneios profissionais. Com sua combinação única de desafio físico, mental e social, o tênis continua a atrair milhões de praticantes em todo o mundo.</p>
